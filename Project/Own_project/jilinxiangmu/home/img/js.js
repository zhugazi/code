//林业要闻
function card(num){
	var i=num;
	document.getElementById("ceng"+i).style.display="";
	document.getElementById("t"+i).className="card_bg1";
		for(var j=1;j<5;j++)
		{
				if(j!=i)
				{
				document.getElementById("ceng"+j).style.display="none";
				document.getElementById("t"+j).className="card_bg2";
				}
			}
}
